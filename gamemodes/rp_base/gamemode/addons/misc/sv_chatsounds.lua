hook("PlayerSay", "Chat Sounds", function(pl, text, teamOnly)
	if pl:IsBanned() then return end

	pl.NextChatSound = RealTime() + 4

	if !pl.NextChatSound or pl.NextChatSound <= RealTime() then
		if not teamOnly and pl:Alive() then
			local sounds = ChatSounds[text:lower()]

			if sounds then
				if pl.LastChatSound then
					pl.LastChatSound:Stop()
				end

				pl.LastChatSound = CreateSound(pl, sounds[math.random(1, #sounds)])
				pl.LastChatSound:Play()
			end
		end
	end
end)