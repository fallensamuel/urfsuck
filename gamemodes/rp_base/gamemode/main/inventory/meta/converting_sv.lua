					proccessUser(data, index + 1)
				--end)
			end
		end)
		
		counter = counter + 1
		
		if counter >= 100 then
			print(index .. ' / ' .. total)
			counter = 0
		end
	end
	
	rp._Stats:Query("SELECT `SteamID`, `Pocket` FROM `player_data` WHERE `Pocket` != '{}';", function(player_data)
		total = #player_data
		proccessUser(player_data, 7401)
	end)
