function PocketToInv_Step1()
	local counter = 0
	local total = 0
	
	local function createInv(data, index)
		if not data[index] then
			print('Done ' .. (index - 1) .. ' users inventory created.')
			return
		end
		
		rp._Inventory:Query("SELECT * FROM inventories WHERE _charID = " .. data[index].SteamID .. " AND _invType = 'player';", function(inventory)
			if #inventory < 1 then
				rp.item.newInv(data[index].SteamID, "player", rp.cfg.InventoryDefault[1], rp.cfg.InventoryDefault[2], function()
					createInv(data, index + 1)
				end)
			else 
				createInv(data, index + 1)
			end
		end)
		
		counter = counter + 1
		
		if counter >= 100 then
			print(index .. ' / ' .. total)
			counter = 0
		end
	end
	
	rp._Stats:Query("SELECT SteamID FROM player_data WHERE `Pocket` != '{}';", function(player_data)
		total = #player_data
		createInv(player_data, 28801)
	end)
end

function PocketToInv_Step2()
	local counter = 0
	local total = 0
	
	local function proccessInv(data, index)
		if not data[index] then
			print('Done ' .. (index - 1) .. ' users inventory graded.')
			return
		end
		
		rp._Inventory:Query("UPDATE inventories SET _height = 5 + ? WHERE _charID = ?;", data[index].Total, util.SteamIDTo64(data[index].SteamID), function()
			proccessInv(data, index + 1)
		end)
		
		counter = counter + 1
		
		if counter >= 100 then
			print(index .. ' / ' .. total)
			counter = 0
		end
	end
	
	rp._Stats:Query("SELECT SteamID, COUNT(SteamID) AS Total FROM kshop_purchases WHERE `Upgrade` = ? GROUP BY SteamID;", "pocket_space_2", function(data)
		total = #data
		proccessInv(data, 1)
	end)
end

function PocketToInv_Step3()
	local counter = 0
	local total = 0
	
	local failed_items = {}
	local failed_total = 0
	local failed_count = 0
	local items_total = 0
	local items_count = 0
	
	local function carryItems(inv, items, index, cback)
		--for k,v in pairs(items) do
			local v = items[ table.GetKeys(items)[index] ]
			
			if not v then
				print('  Done ' .. (items_count - failed_count) .. ' items carried.')
				cback()
				return
			end
			
			if v.contents and v.count then
				if rp.shipments[v.contents] then
					local content = rp.shipments[v.contents].entity
					
					if rp.item.list[content] then
						inv:add(content, v.count, nil, nil, nil, nil, true, function()
							carryItems(inv, items, index + 1, cback)
						end)
					else
						failed_items[content] = true
						
						failed_total = failed_total + 1
						failed_count = failed_count + 1
						--print(content)
						
						carryItems(inv, items, index + 1, cback)
					end
				else
					carryItems(inv, items, index + 1, cback)
				end
			else
				local itemClass = v.Class

				if v.weaponclass then
					itemClass = v.weaponclass
				end

				if v.FoodEnergy and table.Count(rp.item.shop.foods) > 0 then
					itemClass = table.Random(rp.item.shop.foods).uniqueID
				end

				if rp.item.list[itemClass] then
					inv:add(itemClass, nil, nil, nil, nil, nil, nil, function()
						carryItems(inv, items, index + 1, cback)
					end)
				else
					failed_items[itemClass] = true
					
					failed_total = failed_total + 1
					failed_count = failed_count + 1
						
					carryItems(inv, items, index + 1, cback)
				end
			end
		--end
	end
	
	local function proccessUser(data, index)
		if not data[index] then
			PrintTable(failed_items)
			
			print('Done ' .. (index - 1) .. ' users inventory carried, failed ' .. failed_total .. ' / ' .. items_total)
			return
		end
		
		local succ, pocket = pcall(pon.decode, data[index].Pocket)
		local inv
		
		rp._Inventory:Query("SELECT * FROM `inventories` WHERE `_charID` = " .. data[index].SteamID .. " AND `_invType` = 'player';", function(inventory)
			if #inventory > 0 then
				inv = rp.item.createInv(inventory[1]["_width"], inventory[1]["_height"], inventory[1]["_invID"])
				
				items_count = table.Count(pocket)
				items_total = items_total + items_count
				
				print('Proccessing ' .. data[index].SteamID .. ' ' .. items_count .. ' items')
				
				rp.item.restoreInv(inventory[1]["_invID"], inventory[1]["_width"], inventory[1]["_height"], function(inventory)
					failed_count = 0
					
					carryItems(inventory, pocket, 1, function()
						--if index < 50 then
							proccessUser(data, index + 1)
						--end
					end)
				end, inv)
			else
				print('Invalid inventory ' .. data[index].SteamID)
				rp.item.newInv(data[index].SteamID, "player", rp.cfg.InventoryDefault[1], rp.cfg.InventoryDefault[2], function()
					proccessUser(data, index)
				end)
			end
		end)
		
		counter = counter + 1
		
		if counter >= 100 then
			print(index .. ' / ' .. total)
			counter = 0
		end
	end
	
	rp._Stats:Query("SELECT `SteamID`, `Pocket` FROM `player_data` WHERE `Pocket` != '{}';", function(player_data)
		total = #player_data
		
		local start_index = 1
		
		while(player_data[start_index] and tostring(player_data[start_index].SteamID) != '76561198284878255') do
			--print(player_data[start_index].SteamID)
			start_index = start_index + 1
		end
		
		proccessUser(player_data, start_index)
	end)
