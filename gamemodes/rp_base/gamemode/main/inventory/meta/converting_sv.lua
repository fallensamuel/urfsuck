		if not data[index] then
			print('Done ' .. (index - 1) .. ' users inventory carried')
			return
		end
		
		local succ, pocket = pcall(pon.decode, data[index].Pocket)
		local inv
		local need_to_carry = false
		
		for k,v in pairs(pocket) do
			if v.contents and v.count and rp.shipments[v.contents] then
				if to_exchange[ rp.shipments[v.contents].entity ] or to_give[ rp.shipments[v.contents].entity ] then
					need_to_carry = true
					break
				end
			else
				local itemClass = v.Class

				if v.weaponclass then
					itemClass = v.weaponclass
				end

				if to_exchange[ itemClass ] or to_give[ itemClass ] then
					need_to_carry = true
					break
				end
			end
		end
		
		if not need_to_carry then
			proccessUser(data, index + 1)
			return
		end
		
		rp._Inventory:Query("SELECT * FROM `inventories` WHERE `_charID` = " .. data[index].SteamID .. " AND `_invType` = 'player';", function(inventory)
			if #inventory > 0 then
				inv = rp.item.createInv(inventory[1]["_width"], inventory[1]["_height"], inventory[1]["_invID"])
				
